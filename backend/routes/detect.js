const client = new Groq({ apiKey: process.env.GROQ_API_KEY });

// Multer: memory storage, 5MB limit, text files only
const upload = multer({
  storage: multer.memoryStorage(),
  limits: { fileSize: 10 * 1024 * 1024 },
  fileFilter(req, file, cb) {
    const allowed = ["text/plain", "text/html", "text/markdown", "application/pdf",
                     "application/msword",
                     "application/vnd.openxmlformats-officedocument.wordprocessingml.document"];
    if (allowed.includes(file.mimetype) || file.originalname.match(/\.(txt|md|html|htm)$/i)) {
      cb(null, true);
    } else {
      cb(new Error("Unsupported file type. Allowed: .txt, .md, .html, .pdf"));
    }
  },
});

