// ─── Helpers ─────────────────────────────────────────────────────────────────

async function extractFromUrl(url) {
  const res = await axios.get(url, {
    timeout: 12000,
    headers: {
      "User-Agent": "Mozilla/5.0 (compatible; AIDetectorBot/1.0)",
      "Accept": "text/html,application/xhtml+xml",
    },
    maxContentLength: 2 * 1024 * 1024,
  });
  const $ = cheerio.load(res.data);
  $("script, style, nav, header, footer, aside, iframe, noscript, .ad, #ad").remove();
  const article = $("article").text() || $("main").text() || $('[role="main"]').text() || $("body").text();
  return article.replace(/\s+/g, " ").trim();
}

async function extractFromFile(buffer, mimetype, originalname) {
  if (mimetype === "application/pdf" || originalname.toLowerCase().endsWith(".pdf")) {
    try {
      console.log(`[EXTRACT] PDF Buffer size: ${buffer.length}, Type: ${typeof buffer}, isBuffer: ${Buffer.isBuffer(buffer)}`);
      console.log(`[EXTRACT] Magic bytes: ${buffer.slice(0, 4).toString()}`); 
      const data = await pdf(buffer);
      console.log(`[EXTRACT] Extracted ${data.text?.length || 0} characters from PDF`);
      return data.text;
    } catch (err) {
      console.error("PDF Extraction Error Trace:", err);
      throw new Error(`Failed to extract text from PDF: ${err.message}`);
    }
  }
  // For text-based files just decode the buffer
  return buffer.toString("utf-8");
}

async function callGroqDetection(text) {
  const truncated = text.slice(0, 8000); 
  const completion = await client.chat.completions.create({
    model: "llama-3.3-70b-versatile",
    messages: [
      { role: "system", content: SYSTEM_PROMPT },
      { role: "user",   content: `Analyze this text:\n\n${truncated}` }
    ],
    temperature: 0.1,
    max_tokens: 1024,
    response_format: { type: "json_object" }
  });

  const raw = completion.choices[0]?.message?.content || "";
  return JSON.parse(raw);
}

