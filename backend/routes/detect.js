// POST /api/detect/text
router.post("/text", async (req, res) => {
  try {
    const { text } = req.body;
    if (!text || typeof text !== "string") {
      return res.status(400).json({ error: "text field is required" });
    }
    if (text.trim().split(/\s+/).length < 20) {
      return res.status(400).json({ error: "Text too short. Minimum 20 words required." });
    }

    const result = await callGroqDetection(text.trim());
    const id = uuidv4();

    await db.saveScan({
      id,
      source_type: "text",
      source_ref:  null,
      input_text:  text.trim().slice(0, 500),
      word_count:  result.word_count,
      verdict:     result.verdict,
      ai_prob:     result.ai_probability,
      confidence:  result.confidence,
      summary:     result.summary,
      signals:     result.signals,
      ai_flags:    result.ai_flags,
      human_flags: result.human_flags,
      phrases:     result.suspicious_phrases,
      ip_address:  req.ip,
    });

    res.json({ id, ...result });
  } catch (err) {
    console.error("detect/text error:", err);
    res.status(500).json({ error: err.message || "Detection failed" });
  }
});

// POST /api/detect/url
router.post("/url", async (req, res) => {
  try {
    const { url } = req.body;
    if (!url || !/^https?:\/\/.+/.test(url)) {
      return res.status(400).json({ error: "Valid URL is required" });
    }

    const text = await extractFromUrl(url);
    if (text.split(/\s+/).length < 20) {
      return res.status(400).json({ error: "Not enough readable text found at that URL." });
    }

    const result = await callGroqDetection(text);
    const id = uuidv4();

    await db.saveScan({
      id,
      source_type: "url",
      source_ref:  url,
      input_text:  text.slice(0, 500),
      word_count:  result.word_count,
      verdict:     result.verdict,
      ai_prob:     result.ai_probability,
      confidence:  result.confidence,
      summary:     result.summary,
      signals:     result.signals,
      ai_flags:    result.ai_flags,
      human_flags: result.human_flags,
      phrases:     result.suspicious_phrases,
      ip_address:  req.ip,
    });

    res.json({ id, url, ...result });
  } catch (err) {
    console.error("detect/url error:", err);
    const msg = err.code === "ECONNABORTED" ? "URL request timed out" : err.message;
    res.status(500).json({ error: msg || "Detection failed" });
  }
});

// POST /api/detect/file
router.post("/file", upload.single("file"), async (req, res) => {
  try {
    console.log(`[FILE UPLOAD] Received: ${req.file?.originalname} (${req.file?.size} bytes)`);
    if (!req.file) return res.status(400).json({ error: "No file uploaded" });

    const text = await extractFromFile(req.file.buffer, req.file.mimetype, req.file.originalname);
    if (!text || text.trim().split(/\s+/).length < 20) {
      return res.status(400).json({ error: "File contains too little readable text." });
    }

    const result = await callGroqDetection(text.trim());
    const id = uuidv4();

    await db.saveScan({
      id,
      source_type: "file",
      source_ref:  req.file.originalname,
      input_text:  text.trim().slice(0, 500),
      word_count:  result.word_count,
      verdict:     result.verdict,
      ai_prob:     result.ai_probability,
      confidence:  result.confidence,
      summary:     result.summary,
      signals:     result.signals,
      ai_flags:    result.ai_flags,
      human_flags: result.human_flags,
      phrases:     result.suspicious_phrases,
      ip_address:  req.ip,
    });

    res.json({ id, filename: req.file.originalname, ...result });
  } catch (err) {
    console.error("detect/file error:", err);
    res.status(500).json({ error: err.message || "Detection failed" });
  }
});

